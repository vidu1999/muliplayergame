import pygame.draw


class Grid:
    def __init__(self):
        self.gridline= [((0, 60), (600, 60)),
                        ((0, 120), (600, 120)),
                        ((0, 180), (600, 180)),
                        ((0, 240), (600, 240)),
                        ((0, 300), (600, 300)),
                        ((0, 360), (600, 360)),
                        ((0, 420), (600, 420)),
                        ((0, 480), (600, 480)),
                        ((0, 540), (600, 540)),
                        ((0, 600), (600, 600)),
                        ((60, 0), (60, 600)),
                        ((120, 0), (120, 600)),
                        ((180, 0), (180, 600)),
                        ((240, 0), (240, 600)),
                        ((300, 0), (300, 600)),
                        ((360, 0), (360, 600)),
                        ((420, 0), (420, 600)),
                        ((480, 0), (480, 600)),
                        ((540, 0), (540, 600)),
                        ((600, 0), (600, 600)),
                        ]
        self.mddle=[((0, 300), (600, 300))]

    def draw(self,intface):
        for line in self.gridline:
            pygame.draw.line(intface,(0, 0, 255),line[0],line[1],2)
        for line in self.mddle:
            pygame.draw.line(intface, (255, 255, 255), line[0], line[1], 5)